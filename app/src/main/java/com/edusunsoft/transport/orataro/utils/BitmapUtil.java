    public static String getFilename() {
        File file = new File(Environment.getExternalStorageDirectory()
                .getPath(), "Orataro/Images");
        if (!file.exists()) {
            file.mkdirs();
        }
        String uriSting = (file.getAbsolutePath() + "/"
                + System.currentTimeMillis() + ".jpg");
        return uriSting;

    }

